# 十六进制字符串转为字节数组
hex_string = (
    "\x0bX0\x8bw\x02i\xddQ\xb8\x9e_\xbf\x86[\xce\xda\xd6\xd2th\x88Ec>\xd4+B\xf0&C\xf4"
    "k\xcb\x98f\xc6R\xd0\x8d\xd4\x82\r\xe5Oy\xf6j\x8aQ\x0f\x8d>\xe8\x9b\xc8\x83]\xbe"
    "\xbd\r\xe9\x06\x97\x86\xbc\xe4\t(\x1b8D\xc8y\xa7\\\xd6F\x1f\xf0P(_\x11bE\x8b\xb5"
    "\xf7V\xe4b\x02\xa7Je\x1f\xde\x14?r\x91\xe5\x03\"\xd0\x07\x86e\x84\x08\xbd\xac"
    "\xa0zjv@\x14\xab`&\xc2\xb7\xb0\xb4\xf5\xd3\x07"
)

# 转换为字节数组
byte_data = bytes(hex_string, 'latin1')

# 进一步分析字节数据
print("Byte data length:", len(byte_data))
print("Byte data preview:", byte_data[:32])  # 只打印前32个字节作为预览
